// 變數的基本原理

public class foundation{
    public static void main(String[] args){
        // 設一變數a，型態為int，值為1
        int a = 1;
        // 設一變數b，型態為int，值為2
        int b = 2;
        // 將89賦予b
        b = 89;
        // 輸出變數的值
        System.out.println(a);
        System.out.println(b);
    }

}